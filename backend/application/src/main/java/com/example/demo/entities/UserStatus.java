package com.example.demo.entities;

public enum UserStatus {

    WANT_COLLABORATE,          // Пользователь хочет активно сотрудничать и участвовать в проектах
    EXPLORING_OPPORTUNITIES,   // Пользователь изучает разные возможности, пока не определился с конкретным проектом
    OPEN_TO_COLLABORATION,     // Пользователь открыт к предложениям о сотрудничестве, готов обсудить участие
    AVAILABLE_FOR_FEEDBACK,    // Пользователь готов давать советы, консультировать или делиться опытом
    LEADING_PROJECT,           // Пользователь ведет собственный проект, ищет участников или просто руководит
    LOOKING_FOR_TEAM,          // Пользователь ищет команду или людей для совместной работы над проектом
    NOT_AVAILABLE              // Пользователь временно недоступен для сотрудничества или общения
}


